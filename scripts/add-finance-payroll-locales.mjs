async function updateLocales() {
  for (const [lang, translations] of Object.entries(keysToAdd)) {
    const filePath = path.join(localesDir, `${lang}.json`);
    if (fs.existsSync(filePath)) {
      const content = fs.readFileSync(filePath, 'utf-8');
      const json = JSON.parse(content);

      json.nav = { ...(json.nav || {}), ...translations.nav };
      json.common = { ...(json.common || {}), ...translations.common };
      json.finances = { ...(json.finances || {}), ...translations.finances };
      json.payroll = { ...(json.payroll || {}), ...translations.payroll };
      json.journal = { ...(json.journal || {}), ...translations.journal };

      fs.writeFileSync(filePath, JSON.stringify(json, null, 2), 'utf-8');
      console.log(`Updated ${lang}.json`);
    } else {
      console.log(`Warning: ${filePath} not found.`);
    }
  }
}

updateLocales();
